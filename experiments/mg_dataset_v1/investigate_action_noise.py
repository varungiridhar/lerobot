"""Investigate action-sequence smoothness across q5 / q3_termjitter / play.

Motivation: the play bucket is generated by a BC (diffusion) policy whose
denoising stochasticity can inject high-frequency noise into the recorded
actions. MG q5 and q3_termjitter are sourced from human teleop trajectories
that MimicGen warps, and should be much smoother. We don't filter anything
yet — this script just measures what's there so we can pick a mitigation.

Outputs:
  - <out_dir>/action_traces.png    : action(t) per-dim, 5 random demos per bucket
  - <out_dir>/step_delta_dist.png  : histogram of ||a_{t+1} - a_t|| per bucket
  - <out_dir>/spectrum.png         : welch PSD per dim, averaged over demos, per bucket
  - <out_dir>/summary.json         : per-bucket scalar stats
                                     (mean step delta, std step delta, hi-freq ratio)

`hi_freq_ratio` = mean over dims of (sum PSD above 2 Hz) / (sum PSD overall).
A value near 0 means the trajectory is smooth; values near 1 mean it's
dominated by high-frequency content.
"""
from __future__ import annotations

import argparse
import json
from pathlib import Path

import h5py
import numpy as np

DATASET_ROOT = Path(
    "/storage/home/hcoda1/6/vgiridhar6/shared/mimicgen/lerobot_datasets/v1_q5_q3jitter_play"
)
CONTROL_FREQ_HZ = 20.0
HI_FREQ_CUTOFF_HZ = 2.0


def _list_demo_actions(
    hdf5_path: Path,
    max_demos: int | None = None,
    bucket_for_filter: str | None = None,
) -> list[np.ndarray]:
    """Return list of action arrays (T, 7) from one bucket's demo.hdf5.

    If `bucket_for_filter` is provided, the loader-time Savitzky–Golay filter
    is applied via `maybe_filter_actions`. Useful for re-running this
    investigation with the filter on, to confirm the high-frequency energy
    drops to ~MG levels.
    """
    if not hdf5_path.exists():
        return []
    out: list[np.ndarray] = []
    with h5py.File(hdf5_path, "r") as f:
        if "data" not in f:
            return []
        keys = sorted(
            (k for k in f["data"].keys() if k.startswith("demo_")),
            key=lambda x: int(x.split("_")[-1]),
        )
        if max_demos is not None:
            keys = keys[:max_demos]
        for k in keys:
            a = np.asarray(f["data"][k]["actions"])
            if bucket_for_filter is not None:
                from action_filter import maybe_filter_actions
                a = maybe_filter_actions(a, bucket=bucket_for_filter)
            out.append(a)
    return out


def _step_deltas(actions_list: list[np.ndarray]) -> np.ndarray:
    """Concatenate ||a_{t+1} - a_t|| across all demos."""
    deltas = []
    for a in actions_list:
        if len(a) < 2:
            continue
        d = np.linalg.norm(np.diff(a, axis=0), axis=1)
        deltas.append(d)
    return np.concatenate(deltas) if deltas else np.array([])


def _avg_psd(actions_list: list[np.ndarray], fs: float):
    """Average Welch PSD across demos, per action-dim. Returns (freqs, psd[n_dims, F])."""
    from scipy.signal import welch

    if not actions_list:
        return None, None
    n_dims = actions_list[0].shape[1]
    freqs_ref = None
    psd_acc: list[np.ndarray] = [[] for _ in range(n_dims)]
    for a in actions_list:
        if len(a) < 16:
            continue
        nperseg = min(64, len(a))
        for d in range(n_dims):
            f, p = welch(a[:, d], fs=fs, nperseg=nperseg)
            if freqs_ref is None:
                freqs_ref = f
            psd_acc[d].append(p)
    if freqs_ref is None:
        return None, None
    psd = np.stack([np.mean(np.stack(ps), axis=0) for ps in psd_acc])
    return freqs_ref, psd


def _hi_freq_ratio(freqs: np.ndarray, psd: np.ndarray, cutoff_hz: float) -> float:
    """Mean over action dims of fraction of PSD energy above `cutoff_hz`."""
    if freqs is None:
        return float("nan")
    mask = freqs >= cutoff_hz
    total = psd.sum(axis=1) + 1e-12
    hi = psd[:, mask].sum(axis=1)
    return float((hi / total).mean())


def _collect(
    root: Path,
    max_demos: int,
    apply_filter: bool = False,
) -> dict[tuple[str, str, str], list[np.ndarray]]:
    """{(task, bucket, success_label): [actions_demo_arr, ...]}

    When `apply_filter=True`, actions read from the `play` bucket are
    smoothed via `maybe_filter_actions(...)` so we can verify the filter
    actually drops high-frequency content to MG levels.
    """
    out: dict[tuple[str, str, str], list[np.ndarray]] = {}
    for task_dir in sorted(p for p in root.iterdir() if p.is_dir()
                           and p.name in {"square", "threading", "coffee"}):
        task = task_dir.name
        for bucket_dir in sorted(p for p in task_dir.iterdir() if p.is_dir()):
            bucket = bucket_dir.name
            if bucket == "bc_input":
                continue  # not a real bucket; just merged input for BC training
            for fname, label in [("demo.hdf5", "success"), ("demo_failed.hdf5", "failed")]:
                # Two layout candidates: MG nests under <bucket>/<bucket>/, play does not.
                for cand in (bucket_dir / bucket / fname, bucket_dir / fname):
                    if cand.exists():
                        acts = _list_demo_actions(
                            cand,
                            max_demos=max_demos,
                            bucket_for_filter=bucket if apply_filter else None,
                        )
                        if acts:
                            out[(task, bucket, label)] = acts
                        break
    return out


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--root", type=Path, default=DATASET_ROOT)
    ap.add_argument("--out_dir", type=Path, required=True)
    ap.add_argument("--max_demos", type=int, default=20,
                    help="cap demos read per (task, bucket, success) for speed")
    ap.add_argument("--no_plots", action="store_true")
    ap.add_argument("--apply_savgol_to_play", action="store_true",
                    help="Apply the dataloader-time Savitzky-Golay filter to play "
                         "bucket actions on read; use to verify the filter drops "
                         "the high-freq energy down to MG levels.")
    args = ap.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)

    bundles = _collect(args.root, args.max_demos, apply_filter=args.apply_savgol_to_play)
    print(f"savgol on play bucket: {args.apply_savgol_to_play}")
    if not bundles:
        raise SystemExit(f"no demo HDF5s found under {args.root}")

    summary: dict[str, dict] = {}
    print(f"{'task':<10} {'bucket':<16} {'flag':<8}  "
          f"{'n_demo':>6} {'mean_dstep':>10} {'std_dstep':>10} {'hi_freq_ratio':>14}")
    for (task, bucket, label), acts in sorted(bundles.items()):
        ds = _step_deltas(acts)
        freqs, psd = _avg_psd(acts, CONTROL_FREQ_HZ)
        hf_ratio = _hi_freq_ratio(freqs, psd, HI_FREQ_CUTOFF_HZ)
        mean_d = float(ds.mean()) if ds.size else float("nan")
        std_d = float(ds.std()) if ds.size else float("nan")
        key = f"{task}/{bucket}/{label}"
        summary[key] = {
            "n_demos": len(acts),
            "n_steps_total": int(sum(len(a) for a in acts)),
            "mean_step_delta": mean_d,
            "std_step_delta": std_d,
            "hi_freq_ratio": hf_ratio,
        }
        print(f"{task:<10} {bucket:<16} {label:<8}  "
              f"{len(acts):>6d} {mean_d:>10.4f} {std_d:>10.4f} {hf_ratio:>14.4f}")

    (args.out_dir / "summary.json").write_text(json.dumps(summary, indent=2))
    print(f"\nWrote {args.out_dir / 'summary.json'}")

    if args.no_plots:
        return

    # Plots — best-effort, skip if matplotlib missing.
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception as e:
        print(f"matplotlib unavailable, skipping plots: {e}")
        return

    # 1) action(t) traces, one panel per dim, color by bucket.
    #    Show first task only (e.g. square) to keep figure size reasonable.
    pick_task = "square" if any(k[0] == "square" for k in bundles) else next(iter(bundles))[0]
    bucket_picks = [b for (t, b, l) in bundles if t == pick_task and l == "failed"]
    if not bucket_picks:
        bucket_picks = list({b for (t, b, l) in bundles if t == pick_task})
    bucket_picks = sorted(set(bucket_picks))
    n_dims = 7
    fig, axes = plt.subplots(n_dims, 1, figsize=(10, 1.6 * n_dims), sharex=True)
    for bi, bucket in enumerate(bucket_picks):
        for label in ("failed", "success"):
            acts = bundles.get((pick_task, bucket, label))
            if not acts:
                continue
            for a in acts[:3]:
                for d in range(n_dims):
                    axes[d].plot(a[:, d], alpha=0.5, lw=0.8, label=f"{bucket}/{label}" if d == 0 else None)
    for d in range(n_dims):
        axes[d].set_ylabel(f"a[{d}]")
        axes[d].grid(True, alpha=0.3)
    axes[-1].set_xlabel("step")
    axes[0].legend(loc="upper right", fontsize=8)
    fig.suptitle(f"Action traces ({pick_task}) — first 3 demos per (bucket, success-flag)")
    fig.tight_layout()
    fig.savefig(args.out_dir / "action_traces.png", dpi=130)
    plt.close(fig)
    print(f"Wrote {args.out_dir / 'action_traces.png'}")

    # 2) step-delta histogram per bucket (collapsed over task).
    fig, ax = plt.subplots(figsize=(8, 5))
    bucket_to_deltas: dict[str, list[np.ndarray]] = {}
    for (task, bucket, label), acts in bundles.items():
        bucket_to_deltas.setdefault(bucket, []).extend(_step_deltas(acts).tolist() if True else [])
    for bucket, deltas in sorted(bucket_to_deltas.items()):
        d = np.asarray(deltas)
        if d.size == 0:
            continue
        ax.hist(d, bins=80, alpha=0.45, density=True, label=f"{bucket}  (n={d.size})", log=True)
    ax.set_xlabel("||a_{t+1} - a_t||")
    ax.set_ylabel("density (log)")
    ax.set_title("Step-to-step action delta distribution per bucket (all tasks pooled)")
    ax.legend()
    ax.grid(True, alpha=0.3)
    fig.tight_layout()
    fig.savefig(args.out_dir / "step_delta_dist.png", dpi=130)
    plt.close(fig)
    print(f"Wrote {args.out_dir / 'step_delta_dist.png'}")

    # 3) Welch PSD per bucket per dim (averaged across demos / tasks).
    n_dims = 7
    fig, axes = plt.subplots(n_dims, 1, figsize=(10, 1.7 * n_dims), sharex=True)
    bucket_to_acts: dict[str, list[np.ndarray]] = {}
    for (task, bucket, label), acts in bundles.items():
        bucket_to_acts.setdefault(bucket, []).extend(acts)
    for bucket, acts in sorted(bucket_to_acts.items()):
        freqs, psd = _avg_psd(acts, CONTROL_FREQ_HZ)
        if freqs is None:
            continue
        for d in range(n_dims):
            axes[d].semilogy(freqs, psd[d], lw=1.0, label=bucket if d == 0 else None)
    for d in range(n_dims):
        axes[d].set_ylabel(f"PSD a[{d}]")
        axes[d].grid(True, alpha=0.3)
        axes[d].axvline(HI_FREQ_CUTOFF_HZ, color="r", ls=":", alpha=0.5)
    axes[-1].set_xlabel("frequency (Hz)")
    axes[0].legend(loc="upper right", fontsize=8)
    fig.suptitle("Welch PSD of action(t) per dim (averaged across demos, all tasks pooled)")
    fig.tight_layout()
    fig.savefig(args.out_dir / "spectrum.png", dpi=130)
    plt.close(fig)
    print(f"Wrote {args.out_dir / 'spectrum.png'}")


if __name__ == "__main__":
    main()
